export class EmojiMapper {
  private aslMap: EmojiMapping;
  private islMap: EmojiMapping;
  private aslFingerspelling: any;

  constructor() {
    this.aslMap = aslEmojiMap;
    this.islMap = islEmojiMap;
    // Import ASL fingerspelling dynamically to avoid circular dependencies
    import('./ASLHandSigns').then(module => {
      this.aslFingerspelling = new module.ASLFingerspelling();
    });
  }

  mapGlossToEmojis(gloss: string, language: 'ASL' | 'ISL' = 'ASL'): string[] {
    const words = gloss.split(' ').filter(word => word.trim() !== '');
    const emojiMap = language === 'ASL' ? this.aslMap : this.islMap;
    
    return words.map(word => {
      const upperWord = word.toUpperCase();
      
      // First try exact match
      if (emojiMap[upperWord]) {
        return emojiMap[upperWord];
      }
      
      // Try partial matches for compound words
      for (const key in emojiMap) {
        if (upperWord.includes(key) || key.includes(upperWord)) {
          return emojiMap[key];
        }
      }
      
      // Fallback to default
      return emojiMap['DEFAULT'] || '🤟';
    });
  }

  mapGlossToEmojisWithFingerspelling(gloss: string, language: 'ASL' | 'ISL' = 'ASL'): { emojis: string[], fingerspelledWords: string[] } {
  }
  mapGlossToEmojisWithFingerspelling(gloss: string, language: 'ASL' | 'ISL' = 'ASL'): { 
    emojis: string[], 
    fingerspelledWords: string[], 
    regularWords: string[], 
    fingerspellingWords: string[] 
  } {
    const words = gloss.split(' ').filter(word => word.trim() !== '');
    const emojiMap = language === 'ASL' ? this.aslMap : this.islMap;
    const emojis: string[] = [];
    const fingerspelledWords: string[] = [];
    const regularWords: string[] = [];
    const fingerspellingWords: string[] = [];
    
    words.forEach(word => {
      const upperWord = word.toUpperCase();
      
      // First try exact match
      if (emojiMap[upperWord]) {
        emojis.push(emojiMap[upperWord]);
        regularWords.push(upperWord);
        return;
      }
      
      // Try partial matches for compound words
      let found = false;
      for (const key in emojiMap) {
        if (key.length > 2 && (upperWord.includes(key) || key.includes(upperWord))) {
          emojis.push(emojiMap[key]);
          regularWords.push(upperWord);
          found = true;
          break;
        }
      }
      
      // If no match found and word is short (likely a name or specific term), use fingerspelling
      if (!found) {
        if (upperWord.length <= 12 && /^[A-Z0-9\s]+$/.test(upperWord)) {
          // This word should be fingerspelled
          fingerspelledWords.push(upperWord);
          fingerspellingWords.push(upperWord);
          emojis.push('✋'); // Hand placeholder for fingerspelling
        } else {
          // Try to break down the word and find partial matches
          let partialEmojis: string[] = [];
          let remainingWord = upperWord;
          
          // Look for common prefixes and suffixes
          const commonParts = ['UN', 'RE', 'ING', 'ED', 'ER', 'EST', 'LY', 'TION', 'SION'];
          
          for (const part of commonParts) {
            if (remainingWord.includes(part)) {
              remainingWord = remainingWord.replace(part, '');
              if (emojiMap[remainingWord.trim()]) {
                partialEmojis.push(emojiMap[remainingWord.trim()]);
                found = true;
                break;
              }
            }
          }
          
          if (found && partialEmojis.length > 0) {
            emojis.push(...partialEmojis);
            regularWords.push(upperWord);
          } else {
            // Use default emoji
            emojis.push(emojiMap['DEFAULT'] || '❓');
            regularWords.push(upperWord);
          }
          regularWords.push(upperWord);
        }
      }
    });
    
    return { emojis, fingerspelledWords, regularWords, fingerspellingWords };
  }

  getAvailableWords(language: 'ASL' | 'ISL' = 'ASL'): string[] {
    const emojiMap = language === 'ASL' ? this.aslMap : this.islMap;
    return Object.keys(emojiMap).filter(key => key !== 'DEFAULT').sort();
  }

  searchWords(query: string, language: 'ASL' | 'ISL' = 'ASL'): string[] {
    const emojiMap = language === 'ASL' ? this.aslMap : this.islMap;
    const upperQuery = query.toUpperCase();
    
    return Object.keys(emojiMap)
      .filter(key => key !== 'DEFAULT' && key.includes(upperQuery))
      .sort()
      .slice(0, 20); // Limit results
  }
}